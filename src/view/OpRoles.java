    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            if (!validarCamposRol()) return;

            RolesDTO rolDTO = "UPD".equals(mode) ? this.dto : new RolesDTO();
            rolDTO.setNombre(jTextField2.getText().trim());
            rolDTO.setDescripcion(jTextField3.getText().trim());
            rolDTO.setActivo(jCheckBox1.isSelected() ? 1 : 0);

            boolean success = "INS".equals(mode) ? dao.insert(rolDTO) : dao.update(rolDTO);

            if (success) {
                JOptionPane.showMessageDialog(this, "Rol " + ("INS".equals(mode) ? "creado" : "actualizado") + " correctamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                if (parent != null) {
                    parent.refrescarDatos();
                }
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo completar la operación.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            // Uso de logger en vez de imprimir en consola
        }
    }//GEN-LAST:event_jButton2ActionPerformed

